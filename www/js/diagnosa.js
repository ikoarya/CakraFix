  function createQuestionElement(index) {
    var qElement = $('<table border="0" width="100%" height="100%" id="question">');
    
    var questionCategory = '';
    if (index < 14)
      questionCategory = 'Komunikasi';
    else if (index >= 14 && index < 34)
      questionCategory = 'Sosial';
    else if (index >= 34 && index < 52)
      questionCategory = 'Sensorik/Kognitif';
    else if (index >= 52)
      questionCategory = 'Kesehatan/Kebiasaan';

    var radioList = '<td width="60%">';
    var radioTable = '<table width="80%" align="right" border="0" id="listJawaban">';
    var item;
    var input = '';
    for (var i = 0; i < questions[index].choices.length; i++) {
      //item = $('<li id="choice' + i + '">');
      item = '<tr id="choice' + i + '">';
      input = '<td width="10%"><input type="radio" name="answer" value=' + i + ' /></td>';
      input += '<td width="90%">' + questions[index].choices[i] + '</td>';
      item += input + '</tr>\n';
      //item.append(input);
      radioTable += item;
      //radioList.append(radioTable);
    }
    radioTable += '</table>';
    //alert(radioTable);
    radioList += radioTable + '</td>';

    var header = $('<tr><td colspan="3" id="judul" width="100%"><h3>#' + (index + 1) + '</h3> <h4>' + questionCategory + '</h4></td></tr>');
    qElement.append(header);
    
    var question = $('<tr><td width="10%"></td><td width="30%" id="questionBox">' + questions[index].question + '</td>');
    question.append(radioList);
    qElement.append(question);
    return qElement;
  }
  
